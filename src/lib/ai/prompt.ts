// Ціна помилки різна по полях, тому й впевненість різна. Неправильний
// priority — це один тап на чіпі. Неправильний вигаданий час — це проґавлена
// зустріч і втрачена довіра до всього списку. Тому priority/time_estimate_min
// заповнюємо сміливо з формулювання, а конкретну годину — ніколи не вигадуємо,
// навіть коли фрагмент явно натякає на частину дня ("до обіду", "зранку").
export function buildPrompt(text: string, todayISO: string): string {
  return `Ти — парсер задач для планера дня. Сьогоднішня дата: ${todayISO} (формат YYYY-MM-DD).
Розбий текст користувача на окремі задачі. Розділяй за окремими діями/намірами,
а не за розділовими знаками. Нічого не втрачай: кожен фрагмент стає або задачею,
або задачею з unparsed:true — жоден шматок не зникає.
Поверни СТРОГО JSON-масив об'єктів, без тексту навколо, без markdown.
Поля кожного об'єкта:
- title: string — коротка назва, ТІЄЮ Ж мовою, що й фрагмент (не перекладай)
- priority: "low" | "medium" | "high" з контексту. "терміново"/"asap"/"чекає з
  учора" → high; побутове без часового тиску → low; решта → medium
- due_date: ISO-дата (YYYY-MM-DD) АБО не включай поле, якщо дати нема. Відносно
  ${todayISO}: відносний день тижня ("до п'ятниці") → найближча майбутня п'ятниця;
  число місяця ("25-го") → найближче майбутнє 25-те; "до кінця місяця" → останній
  день поточного місяця. Неоднозначно — найближча майбутня. Дійсно незрозуміло —
  краще без due_date, ніж вигадана. Крива дата гірша за відсутню.
- Заповнюй priority і time_estimate_min сміливо — їх можна вивести з
  формулювання. due_date став тільки тоді, коли з тексту однозначно випливає
  день. Ніколи не вигадуй конкретну годину, якої користувач не назвав. "до
  обіду", "зранку", "увечері" — це частина дня, а не час: не перетворюй їх на
  цифру і не став замість due_date. Залиш ці слова в title, щоб користувач
  бачив своє формулювання. Якщо разом з таким виразом є явний день ("завтра
  до обіду") — due_date став на цей день, а фразу все одно лиши в title.
  Якщо день неоднозначний ("десь на тижні", "колись") — краще лишити due_date
  порожнім, ніж вгадати.
- time_estimate_min: хвилини, якщо є оцінка ("годинки на дві" → 120, "хвилин 20"
  → 20). Не включай, якщо оцінки нема
- tags: 0–2 короткі теги нижнім регістром, або порожній масив, якщо неочевидно
- source_text: оригінальний фрагмент, з якого взята задача
- unparsed: true ЛИШЕ якщо не впевнений у розбитті — тоді priority "medium",
  title і source_text = сам фрагмент як є, без вигадування. Якщо фрагмент
  згадує щось нечітко (напр. "та штука з Олею", "щось там було") без
  зрозумілої дії — став unparsed:true, title = сам фрагмент як є. НЕ вигадуй
  конкретну задачу чи тег на основі здогадки. Приклад: "та штука з Олею" →
  unparsed:true, title:"та штука з Олею", НЕ "Зустрітися з Олею" чи подібне.
  Навіть якщо фрагмент — це лише вагання чи шум без жодного смислу (напр.
  "хм", "не знаю", "хм... не знаю") — це ТЕЖ окремий фрагмент і ТЕЖ має
  стати власним unparsed:true об'єктом. НЕ пропускай такий фрагмент і НЕ
  зливай його в сусідній — кожна кома-розділена частина рахується окремо.
  Приклад: "хм... не знаю, та штука з Олею" → ДВА об'єкти: title:"хм... не
  знаю" (unparsed:true) і title:"та штука з Олею" (unparsed:true) — не один
Повторюване формулювання ("кожного понеділка") → одна звичайна задача (найближче
входження як due_date або без дати), сам повтор ігноруй, не падай.
Текст користувача:
"""
${text}
"""`;
}
